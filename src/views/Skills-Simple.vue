            <hr/>
            <div class="row p-2">    
                <div class="col-4 text-left" v-for='item in allSkills.filter(item => item.type == "language")' :key='item.title'>
                    <div class='card mb-2 p-2'>{{item.name}}</div>
                </div>
            </div>

            <!-- Front End Frameworks  -->
            <br/><h5 class="text-left">Front End Frameworks</h5>
            <hr/>
            <div class="row p-2">
